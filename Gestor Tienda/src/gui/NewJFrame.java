    private void btnBuscarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarProductoActionPerformed
        try {
            MantenedorProducto con = new MantenedorProducto();

            if (radPoster.isSelected()) {
                Poster poster = con.buscarPoster(txtCodigoInsertar.getText());
                if (poster.getDimension() != null) {
                    txtNombreInsertar.setText(poster.getNombre());
                    txtPrecioInsertar.setText(String.valueOf(poster.getPrecio()));
                    txtDimensionesInsertar.setText(poster.getDimension());
                    txtArtistaInsertar.setText("");
                    txtAnnioInsertar.setText("");
                } else {
                            {
                                JOptionPane.showMessageDialog(null, "No se encontro el poster con  ese codigo");
                            }
                }
            } else if (radDisco.isSelected()) {
                Disco disco = con.buscarDisco(txtCodigoInsertar.getText());
                if (disco.getArtista() != null) {
                    txtNombreInsertar.setText(disco.getNombre());
                    txtPrecioInsertar.setText(String.valueOf(disco.getPrecio()));
                    txtArtistaInsertar.setText(disco.getArtista());
                    txtAnnioInsertar.setText(String.valueOf(disco.getAnnio()));
                    txtDimensionesInsertar.setText("");
                } else {
                    JOptionPane.showMessageDialog(null, "No se encontro el disco con  ese codigo");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Debe ingresar un codigo y seleccionar el tipo de producto");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarProductoActionPerformed

