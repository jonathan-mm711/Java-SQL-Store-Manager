    private void btnListarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarVentaActionPerformed
        txaRegistroVentas.setText("");
        IngresarVenta listarVenta = new IngresarVenta();
        RegistroVentas regVenta = new RegistroVentas();
        int numVent;
        int subTotal = 0;
        try {
            ArrayList<Venta> listNumVenta = regVenta.listNumVenta();
            for (Venta numV : listNumVenta) {
                numVent = numV.getNumero();
                ArrayList<Venta> ventaActual = listarVenta.ventaNumVenta(numVent);
                txaRegistroVentas.append("Numero de Venta: " + numVent + "\n\n");
                for (Venta vta : ventaActual) {
                    Disco producto = new Disco();
                    producto = listarVenta.buscarProducto(vta.getProducto().getCodigo());
                    txaRegistroVentas.append("Código Producto: " + vta.getProducto().getCodigo() + "\n");
                    txaRegistroVentas.append("Nombre Producto: " + producto.getNombre() + "\n");
                    txaRegistroVentas.append("Precio Producto: " + producto.getPrecio() + "\n");
                    txaRegistroVentas.append("Fecha agregado: " + vta.getFecha() + "\n");
                    txaRegistroVentas.append("   ---" + "\n");
                    subTotal = subTotal + producto.getPrecio();
                }
                txaRegistroVentas.append("_______________________" + "\n");
                txaRegistroVentas.append("SUBTOTAL: $" + subTotal + "\n\n");

                if (subTotal >= 20000) {
                    txaRegistroVentas.append("Se aplicó un descuento de 10% por comprar más de $20.000!\n\n");
                }
                txaRegistroVentas.append("TOTAL A PAGAR: $" + listarVenta.calcularTotalConDescuento(subTotal) + "\n");
                txaRegistroVentas.append("_______________________" + "\n");
                txaRegistroVentas.append("_______________________" + "\n");
                subTotal = 0;
            }
        } catch (SQLException ex) {
            Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnListarVentaActionPerformed

