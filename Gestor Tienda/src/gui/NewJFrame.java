    private void btnInsertarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarProductoActionPerformed
        try{
            Poster poster = new Poster();
            Disco disco = new Disco();
            MantenedorProducto con = new MantenedorProducto();

            //El if
            if(radPoster.isSelected())
            {
                txtAnnioInsertar.setText("");
                txtArtistaInsertar.setText("");
                poster.setCodigo(txtCodigoInsertar.getText());
                poster.setNombre(txtNombreInsertar.getText());
                poster.setPrecio(Integer.parseInt(txtPrecioInsertar.getText()));
                poster.setDimension(txtDimensionesInsertar.getText());
                con.insertarPoster(poster);
                JOptionPane.showMessageDialog(null, "Producto Ingresado");
                txtCodigoInsertar.requestFocus();
                limpiarControles();
                
            }

            else if(radDisco.isSelected())
            {
                txtDimensionesInsertar.setText("");                
                disco.setCodigo(txtCodigoInsertar.getText());
                disco.setNombre(txtNombreInsertar.getText());
                disco.setPrecio(Integer.parseInt(txtPrecioInsertar.getText()));
                disco.setArtista(txtArtistaInsertar.getText());
                disco.setAnnio(Integer.parseInt(txtAnnioInsertar.getText()));
                con.insertarDisco(disco);
                JOptionPane.showMessageDialog(null, "Producto Ingresado");
                txtCodigoInsertar.requestFocus();
                limpiarControles();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Debe  seleccionar el tipo de producto");
            }
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }//GEN-LAST:event_btnInsertarProductoActionPerformed

